class Hour {
  final int month;
  final int day;
  final int hour;
  final String ampm;
  final String icon;
  final int temp;
  final int humidity;
  final int windSpeed;

  const Hour({
    required this.month,
    required this.day,
    required this.hour,
    required this.ampm,
    required this.icon,
    required this.temp,
    required this.humidity,
    required this.windSpeed,
  });
}
